package javafundamental;

public class OperatorsExample {
	public static void main(String[] args) {
		int x = 10;
		x += 5;
		System.out.println(x);

		int z = 5;
		z *= 3;
		System.out.println(z);
		
		int y = 5;
	    ++y;
	    System.out.println(y);
	    
	    int a = 5;
	    --a;
	    System.out.println(a);
	}

}
